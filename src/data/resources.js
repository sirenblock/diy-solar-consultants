// Helper functions
export function getResourcesByCategory(categoryId) {
  return resources.filter(r => r.category === categoryId);
}

export function getFeaturedResources() {
  return resources.filter(r => r.featured === true);
}

export function searchResources(query) {
  const lowerQuery = query.toLowerCase();
  return resources.filter(r =>
    r.title.toLowerCase().includes(lowerQuery) ||
    r.description.toLowerCase().includes(lowerQuery) ||
    r.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
}

export function getResourceById(id) {
  return resources.find(r => r.id === id);
}

export function getRelatedResources(resourceId) {
  const resource = getResourceById(resourceId);
  if (!resource || !resource.relatedResources) return [];
  return resource.relatedResources.map(id => getResourceById(id)).filter(Boolean);
}
